/** Get mock listings filtered by city */
export function getMockListingsByCity(city: string): Listing[] {
  return mockListings.filter(
    (l) => l.City.toLowerCase() === city.toLowerCase()
  );
}

/** Get mock listings that have open houses */
export function getMockOpenHouses(): Listing[] {
  return mockListings.filter((l) => l.OpenHouseStartTime);
}

/** Get mock featured listings (highest price) */
export function getMockFeatured(): Listing[] {
  return [...mockListings]
    .sort((a, b) => (b.ListPrice || 0) - (a.ListPrice || 0))
    .slice(0, 8);
}

/** Get a single mock listing by key */
export function getMockListing(id: string): Listing | null {
  return mockListings.find((l) => l.ListingKey === id) || null;
}
